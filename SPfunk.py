from msvcrt import getwch

def ask_question(question):
    print(question['question'])
    print("Alternativ:")
    for key in ['a', 'b', 'c', 'd']:
        if key in question:
            print(f"{key}: {question[key]}")

    while True:
        print("Skriv ditt svar (a, b, c eller d):")
        player_answer = getwch().lower()
        if player_answer in ['a', 'b', 'c', 'd']:
            return player_answer
        else:
            print("Ogiltig inmatning. Vänligen ange a, b, c eller d.")

